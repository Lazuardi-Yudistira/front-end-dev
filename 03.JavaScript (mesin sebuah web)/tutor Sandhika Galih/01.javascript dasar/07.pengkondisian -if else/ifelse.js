// deklarasi angka
var angka = 2;

// jika angka dibagi 2 sisa baginya sama dengan 0 (true)
// maka, maka tampilakan(alert) bilangan GENAP dalam pop up
// jika tidak(false) maka tampilkan(alert) bilangan GANJIL dalam pop up
if (angka % 2 == 0) {
  alert(angka + " adalah bilangan GENAP");
} else {
  alert(angka + " adalah bilangan GANJIL");
}
